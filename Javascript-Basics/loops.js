// for loop
for(let i = 0; i<=5; i++){
    document.write(i + " ");
}
document.write("<br/>");

// while loop
var a = 11;
while(a<=15)
{
    document.write(a+" ");
    a++;
}
document.write("<br/>");

// do while loop
var a = 22;
do{
    document.write(a+" ");
}
while(a == 20);
document.write("<br/>");